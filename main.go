package main

////////////////////////////////////////////////////////////////////////////////
// DEPENDENCIES
////////////////////////////////////////////////////////////////////////////////

import (
	"fmt"
	"os"
	"os/user"

	"github.com/klaytonkowalski/example-interpreter/repl"
)

////////////////////////////////////////////////////////////////////////////////
// FUNCTIONS
////////////////////////////////////////////////////////////////////////////////

func main() {
	user, err := user.Current()
	if err != nil {
		panic(err)
	}
	fmt.Printf("Bonvenon, %s. This is the Monkey programming language.\n", user.Username)
	repl.Start(os.Stdin, os.Stdout)
}
